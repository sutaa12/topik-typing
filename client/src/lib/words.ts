export function getWordsForLevel(level: string): Word[] {
  const raw = wordsData[level] || [];
  return raw.map(([korean, japanese, katakana, romanization]) => ({
    korean,
    japanese,
    katakana,
    romanization,
    level: parseInt(level),
  }));
}

